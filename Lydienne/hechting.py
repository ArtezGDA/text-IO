#!/usr/bin/env python
# encoding: utf-8

import subprocess

word="Hechting"
stdout, stderr = subprocess.Popen(
                     ['cowsay', word]).communicate()

word="Hallo, ik heb je gemist. Opvallend hoe snel ik aan jou gehecht ben, terwijl we elkaar nog maar een paar weken kennen. Maar ja, dat komt door mij. Ik hecht mij heel snel. Volgens sommigen te snel… Ik weet nog hoe ik mij als kind zo aan een cavia hechtte.."
stdout, stderr = subprocess.Popen(
                     ['cowsay', '-f', 'kitty', word]).communicate()

word="Je gaat me toch niet vergelijken met een cavia?"
stdout, stderr = subprocess.Popen(
                     ['cowsay', '-f', 'meow', word]).communicate()

word="Nee, nee, ik bedoel niet dat je een cavia bent ofzo… ik bedoel gewoon dat ik me heel snel hecht, zelfs aan iets onbenulligs als een cavia…"
stdout, stderr = subprocess.Popen(
                     ['cowsay', '-f', 'kitty', word]).communicate()

word="Ik vind het niet fijn dat je me als iets onbenulligs bestempelt…"
stdout, stderr = subprocess.Popen(
                     ['cowsay', '-f', 'meow', word]).communicate()

word="Oh, nee, ik bedoel niet dat jij onbenullig bent, ik wil eigenlijk gewoon maar zeggen dat ik me zelfs aan kleine diertjes snel hecht en dat …"
stdout, stderr = subprocess.Popen(
                     ['cowsay', '-f', 'kitty', word]).communicate()

word="Ik heb altijd moeite gehad met mijn lengte en ik waardeer het niet dat jij mijn lengte op die manier weer eens in de kijker zet…"
stdout, stderr = subprocess.Popen(
                     ['cowsay', '-f', 'meow', word]).communicate()

word="Oh, nee, ik bedoel met klein zeker niet dat jij klein bent ofzo…"
stdout, stderr = subprocess.Popen(
                     ['cowsay', '-f', 'kitty', word]).communicate()

word="Da’s nu al de tweede keer dat je zegt dat ik klein ben, terwijl je maar al te goed weet dat ik daar een minderwaardigheidscomplex over heb…"
stdout, stderr = subprocess.Popen(
                     ['cowsay', '-f', 'meow', word]).communicate()

word="Ik wil jou niet kwetsen. Integendeel, ik wil eigenlijk gewoon zeggen dat ik je gemist heb omdat ik al na een paar bijeenkomsten aan jou was gehecht…"
stdout, stderr = subprocess.Popen(
                     ['cowsay', '-f', 'kitty', word]).communicate()

word="En je zegt daar ook direct bij dat je je te snel hecht , dus vorm ik een probleem voor jou…"
stdout, stderr = subprocess.Popen(
                     ['cowsay', '-f', 'meow', word]).communicate()

word="Oh, nee, dat heb je verkeerd begrepen, ik …"
stdout, stderr = subprocess.Popen(
                     ['cowsay', '-f', 'kitty', word]).communicate()

word="Wil je nu ook nog zeggen dat ik dom ben omdat ik iets niet goed begrijp…?"
stdout, stderr = subprocess.Popen(
                     ['cowsay', '-f', 'meow', word]).communicate()

word="Nee, nee, ik zeg alleen dat sommige mensen vinden dat ik mij TE snel hecht…."
stdout, stderr = subprocess.Popen(
                     ['cowsay', '-f', 'kitty', word]).communicate()

word="En met die ‘sommige mensen’ bedoel je mij zeker want ik heb dat zopas gezegd en die mensen die zoiets zeggen, vind jij natuurlijk dom!"
stdout, stderr = subprocess.Popen(
                     ['cowsay', '-f', 'meow', word]).communicate()

word="Luister, ik wil eigenlijk gewoon lief zijn tegen jou door te zeggen dat ik je gemist hebt…"
stdout, stderr = subprocess.Popen(
                     ['cowsay', '-f', 'kitty', word]).communicate()

word="Zeg dat dan gewoon, hé, in plaats van mij te vergelijken met een dikke, domme cavia!"
stdout, stderr = subprocess.Popen(
                     ['cowsay', '-f', 'meow', word]).communicate()

