def karakters (n):
    o=""
    key = {'a':'@', 'b':'Q', 'c':'>', 'd':'<', 'e':'?', 'f':'/', 'g':'=', 'h':'+', 
       'i':')', 'j':'(', 'k':'#', 'l':'$', 'm':'%', 'n':'^', 'o':'&', 'p':'*', 
       'q':'[', 'r':']', 's':'{', 't':'}', 'u':';', 'v':'~', 'w':',', 'x':'.',
       'y':'ë', 'z':'æ', 'A':'<>', 'B':'**', 'C':'$$', 'D':'>@', 'E':'2', 'F':'#_', 
       'G':'-', 'H':'_', 'I':'__', 'J':'±', 'K':'++', 'L':'7', 'M':'{}', 'N':'11', 
       '§':'?', 'P':'66', 'Q':'``', 'R':'+--', 'S':'oo', 'T':'S', 'U':'+++++', 'V':'xx', 
       'W':'xy', 'X':'Kkk', 'Y':'0_', 'Z':'))'}
    for x in n:
        v = x in key.keys()
